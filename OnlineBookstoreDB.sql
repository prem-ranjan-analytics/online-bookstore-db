-- 13. Calculate the percentage of stock sold for each book:
SELECT b.Book_ID,
       b.Title,
       ROUND((COALESCE(SUM(o.Quantity),0) / b.Stock) * 100,2) AS Percentage_Sold
FROM Books b
LEFT JOIN Orders o ON b.Book_ID = o.Book_ID
GROUP BY b.Book_ID, b.Title, b.Stock;

-- 14. Calculate the stock remaining after fulfilling all orders:

SELECT b.Book_ID,
       b.Title,
       b.Stock AS Original_Stock,
       COALESCE(SUM(o.Quantity), 0) AS Total_Ordered,
       b.Stock - COALESCE(SUM(o.Quantity), 0) AS Remaining_Stock
FROM Books b
LEFT JOIN Orders o ON b.Book_ID = o.Book_ID
GROUP BY b.Book_ID, b.Title, b.Stock;