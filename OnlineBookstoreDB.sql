-- Advance Questions:

-- 1. Retrieve the total number of books sold for each genre:

SELECT b.Genre, 
       SUM(o.Quantity) as "Total_Books_Sold"
FROM Orders o
JOIN Books b ON o.Book_ID = b.Book_ID
GROUP BY b.Genre;

-- 2. Display books whose remaining stock is less than 5:

SELECT b.Book_ID,
       b.Title,
       b.Stock - COALESCE(SUM(o.Quantity),0) AS Remaining_Stock
FROM Books b
LEFT JOIN Orders o ON b.Book_ID = o.Book_ID
GROUP BY b.Book_ID, b.Title, b.Stock
HAVING Remaining_Stock < 5;

-- 3. Display oversold books (ordered quantity > stock):

SELECT b.Book_ID,
       b.Title,
       b.Stock,
       COALESCE(SUM(o.Quantity),0) AS Total_Ordered
FROM Books b
LEFT JOIN Orders o ON b.Book_ID = o.Book_ID
GROUP BY b.Book_ID, b.Title, b.Stock
HAVING COALESCE(SUM(o.Quantity),0) > b.Stock;

-- 4. Find the average price of books in the "Fantasy" genre:

